						<h4>
							Blockchain is a digital decentralised public ledger that is used to log transactions into ‘blocks’ in chronological order. A blockchain network is controlled by a network of devices spread across the globe. <br>
							When a transaction occurs between two parties it is recorded on the blockchain and cannot be altered without changing all the blocks that follow, which cannot be done due to the decentralised nature of blockchain. The transactions occur peer to peer, removing the requirement to involve a trusted third party. Blockchain has no centralised entry point because data is stored across the network of computers worldwide, therefore removing the risk of hacking. All the users of the network can see entries on public blockchains, if they are configured with this feature (Lo, 2017). Therefore, blockchain is tamper proof, reliable, and transparent, which can provide great benefits to the pharmaceutical industry.
